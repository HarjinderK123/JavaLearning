package Test20June;

public class Arithmetic_opt {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
//arthmatical operation
        int sum = a+b;
        int sub = a-b;
        int product = a*b;
        int quotient = a /b;
        int remainder = a % b;
        System.out.println("Value of a+b :" + sum );
        System.out.println("Value of a*b :" + product);
        System.out.println("Value of a/b :" + quotient );
        System.out.println("Value of a-b :" + sub );
        System.out.println("Value of a%b :" + remainder );
    }

}
