package ex_09_switch;

public class Lab096_Interview {
    public static void main(String[] args) {
        char code = 'C';
        switch (code){
            case 'A':
                System.out.println("hello");
                break;
            case 'C':
                System.out.println("66");
                break;
            default:
                System.out.println("uuy");
                break;

        }
        }
    }
