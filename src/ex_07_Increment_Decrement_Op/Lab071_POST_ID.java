package ex_07_Increment_Decrement_Op;

public class Lab071_POST_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        System.out.println(a);
        // POST increment  = Print first and then increase
        int b = 11;
        System.out.println(b++);
        System.out.println(b);

    }
}
